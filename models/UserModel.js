const mongoose = require("../db/connection.js")




//// DEFINE SCHEMA ////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

const UserSchema = new mongoose.Schema({
  username: String,
  favorites: [
    {
      beerId: String
    }
  ],
  photo: String
});




//// DEFINE MODEL ////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

const UserModel = mongoose.model("User", UserSchema)






//// EXPORT MODULES ///////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

module.exports = UserModel







